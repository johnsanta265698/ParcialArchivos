    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigo = txfCodigo.getText();
        Ciudad objCiudad = new Ciudad(codigo,"","","");
        controlCiudad objControlCiudad = new controlCiudad(objCiudad);
        objCiudad = objControlCiudad.consultar();
        txfNombre.setText(objCiudad.getNombre());
        txfCodigoPais.setText(objCiudad.getCodigoPais());
        txfRutaNombre.setText(objCiudad.getRutaNombre());
        this.listar();
    }//GEN-LAST:event_btnBuscarActionPerformed

